const container = document.getElementById('container');
const select = document.getElementById('select');
const input = document.getElementById('input');
let btn = document.getElementById('button');


function showMovies(data) {
  container.innerHTML = '';

  data.forEach(movie => {
    const card = document.createElement('div');

    card.classList.add(
      "w-[244px]",         // width barqaror
      "h-[520px]",         // height barqaror
      "bg-[#333333]",
      "rounded-lg",
      "p-3",
      "flex",
      "flex-col",
      "items-center",
      "text-center",
      "shadow-lg",
      "overflow-hidden"   // ortiqcha matn chiqmasligi uchun
    );

    card.innerHTML = `
      <img src="image copy.png" alt="${movie.Title}" class="w-full h-[300px] object-cover rounded-lg" />
      <h1 class="font-bold text-white text-lg mt-2 line-clamp-2">${movie.Title}</h1>
      <h2 class="text-base font-normal text-white mt-2">
        ${movie.imdb_rating || "?"} · ${movie.movie_year || "?"} · ${movie.runtime ? Math.floor(movie.runtime / 60) + "hr" + (movie.runtime % 60) + "min" : "?"}
      </h2>
      <span class="text-white font-normal text-sm mt-2 line-clamp-2">${movie.Categories || "Unknown"}</span>
      <button class="w-[90px] h-[35px] flex justify-center items-center text-white text-sm font-medium bg-[#5CB85C] rounded-sm mt-auto hover:bg-green-600 transition-all cursor-pointer">
        More info
      </button>
    `;

    container.appendChild(card);
  });
}
showMovies(data);

btn.addEventListener("click", () => {
  // console.log(value);
  
  let inpValue = input.value.toLowerCase();
  let filteredCards = data.filter((value) =>
    value.Title.toLowerCase().includes(inpValue)
  );

  showMovies(filteredCards);
});


select.addEventListener("change", (e) => {
  let sorted = [...data];

  if (e.target.value == "A-Z") {
    sorted.sort((a, b) => a.Title.localeCompare(b.Title));
    showMovies(sorted);
  } else if (e.target.value == "Z-A") {
    sorted.sort((a, b) => b.Title.localeCompare(a.Title));
    showMovies(sorted);
  }
});